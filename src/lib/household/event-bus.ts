// =============================================================================
// Farther Unified Platform — Event Bus
// =============================================================================
//
// Cloud Pub/Sub style event system for cross-platform communication.
// Stage 1: In-memory pub/sub with full event logging and replay.
// Stage 2: Google Cloud Pub/Sub with dead-letter topics and ordering keys.
//
// Events flow:
//   Data mutation → Event Bus → Subscribers
//     → Planning Engine (recompute projections)
//     → Proposal System (invalidate active proposals)
//     → HubSpot Sync (push CRM updates)
//     → Client Portal (real-time notifications)
//     → Audit Log (compliance trail)
//
// This module is self-contained — no React, Next.js, or Prisma dependencies.
//
// @module household/event-bus
// =============================================================================

